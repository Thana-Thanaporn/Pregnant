package pudpongsai.thanaporn.th.ac.su.reg.pregnant.Details;

public class TelDetail {

    public String NameTel;
    public String Tel;

    public TelDetail(String nameTel, String tel) {
        NameTel = nameTel;
        Tel = tel;
    }

    public String getNameTel() {
        return NameTel;
    }

    public void setNameTel(String nameTel) {
        NameTel = nameTel;
    }

    public String getTel() {
        return Tel;
    }

    public void setTel(String tel) {
        Tel = tel;
    }
}
